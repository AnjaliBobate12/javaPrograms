package loops;

public class ElseIf {
	
	public static void main(String[] args)
	{
		int i=30;
		
		if(i<=10)
		{
			System.out.print("i is less than 10");
			
		}
		else if(i<=30)
		{
			System.out.print("i is less than 30");
		}
		else if(i<=60)
		{
			System.out.print("i is less than 60");
		}
			else
		{
			System.out.print("i is not satisfing any condition");
		}
			
	}

}
